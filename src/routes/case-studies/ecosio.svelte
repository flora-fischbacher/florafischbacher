        <h4 class="imageText-heading">Re-Design Blog Posts</h4>
        <p>
          Once the blog overview was complete, the next crucial step was to
          re-design the blog posts themselves. The original layout only
          consisted of the article content, which was quite difficult to read as
          all headings looked very similar.
          <br /><br />
          The new design ensured that each heading could be differentiated and also
          included elements to improve the user experience and keep the user engaged
          after completing the article.
          <br /><br />
          Check below to find out which elements were added to the blog posts!
        </p>
      </div>
    </div>
  </div>
  <div class="grid grid-cols-12 gap-6 mt-4 md:mt-16">
    <div class="col-span-12 lg:col-span-4">
      <div class="text-grid">
        <h3 class="tagline">Step 1</h3>
        <p><b>Blog Sidebar</b></p>
        <p>
          To increase user engagement and conversion rates, the new design
          contained a sidebar showcasing relevant webinars, gated content, and
          related articles.
          <br /><br />
          The sidebar was implemented as a sticky element so that users could easily
          find the information while scrolling through the article.

          <br /><br />
          After this simple feature was implemented, we immediately saw a spike in
          conversions!
        </p>
      </div>
    </div>
    <div class="col-span-12 lg:col-span-4">
      <div class="text-grid">
        <h3 class="tagline">Step 2</h3>
        <p><b>Sticky Table of Contents</b></p>
        <p>
          Similar to the blog sidebar, a table of contents was introduced to the
          blog posts which would improve the user experience by showing them
          where they are in the article.
          <br /><br />
          The table of contents would also be sticky and accompany users scrolling
          through the article. To keep the design compact, it would also be collapsible,
          meaning that only H2s would be shown until scrolled to the relevant section
          where it would open to show all sub-headings in that section.
        </p>
      </div>
    </div>
    <div class="col-span-12 lg:col-span-4">
      <div class="text-grid">
        <h3 class="tagline">Step 3</h3>
        <p><b>Feedback section</b></p>
        <p>
          User feedback is one of the most important features in any design, it
          is the best way to determine if the information you provided was
          relevant. Therefore, a feedback section was introduced at the end of
          each blog post asking visitors for their feedback on the article and
          website in general.
          <br /><br />
          This is crucial to be able to improve the usability even more in the future.
          <br /><br />
